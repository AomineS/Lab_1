# . Відомо, що X кг цукерок коштують A гривень. Визначити, скільки коштує 1 кг і Y кг цих же
# цукерок.
X = int(input("Ввести кількість в кг X: "))
A = int(input("Ввести ціну за цукерки A: "))
Y = int(input("Ввести кількість цукерок в кг Y: "))
S=A/X
print('ціна 1 кг цукерок', S)
print('ціна Y кг цукерок', Y*S)
