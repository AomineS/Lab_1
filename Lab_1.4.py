# Дані координати двох протилежних вершин прямокутника: (x1, y1), (x2, y2). Сторони
# прямокутника паралельні до осів координат. Знайти периметр і площу даного прямокутника.
x1 = int(input("Ввести x1: "))
y1 = int(input("Ввести y1: "))
x2 = int(input("Ввести x2: "))
y2 = int(input("Ввести y2: "))
AA = y2 - y1
BB = x2 - x1
P = 2*(AA+BB)
S = AA * BB
print('Периметр прямокутника: ', P)
print('Площа прямокутника: ', S)


